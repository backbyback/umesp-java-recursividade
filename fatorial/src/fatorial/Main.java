    int fatorial(int numero) {
        if (numero > 5) {
            return 0;
        } else {
            fatorial = fatorial * numero;
            System.out.println("Fatorial " + numero + " = " + fatorial);
            return numero + fatorial(numero + 1);
        }
    }

